import re

password = input("Введіть пароль: ")
if not re.search(r"^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$", password):
    print("Пароль недостатньо надійний")
    print("Пароль має містити мінімум 8 символів, букви верхнього і нижнього регістру, цифри, та спеціальні символи")
else:
    print("Пароль надійний")
